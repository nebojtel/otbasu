create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text unique,
  full_name text,
  role text not null default 'content_manager' check (role in ('admin', 'content_manager')),
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.settings (
  id integer primary key default 1 check (id = 1),
  "storeName" text default 'ОТБАСЫ',
  eyebrow text default 'Instagram витрина',
  "heroTitle" text default 'Товары для дома и семьи',
  "heroButtonText" text default 'Смотреть товары',
  "catalogTitle" text default 'Популярные товары',
  "searchPlaceholder" text default 'Найти товар из обзора...',
  "kaspiStoreUrl" text default '',
  "kaspiStoreTitle" text default 'Все товары ОТБАСЫ',
  "kaspiStoreSubtitle" text default 'Открыть магазин на Kaspi',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.categories (
  id uuid primary key default gen_random_uuid(),
  name text not null unique,
  sort integer not null default 50,
  is_active boolean not null default true,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.products (
  id uuid primary key default gen_random_uuid(),
  title text not null,
  category_id uuid references public.categories(id) on delete set null,
  category text not null default 'Без категории',
  tag text not null default 'none' check (tag in ('none', 'hit', 'new', 'promo')),
  status text not null default 'draft' check (status in ('active', 'draft', 'hidden')),
  image_url text default '',
  images jsonb not null default '[]'::jsonb,
  kaspi_url text default '',
  video_url text default '',
  sort integer not null default 100,
  note text default '',
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.analytics_events (
  id bigint generated by default as identity primary key,
  event_type text not null check (event_type in ('product_view', 'video_click', 'kaspi_click', 'tab_click', 'filter_change', 'gallery_open')),
  product_id uuid references public.products(id) on delete set null,
  session_id text,
  tab text,
  category_filter text,
  meta jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);


create or replace function public.handle_new_user_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, full_name, role, is_active)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'full_name', ''),
    case when (new.raw_user_meta_data ->> 'role') in ('admin', 'content_manager') then new.raw_user_meta_data ->> 'role' else 'content_manager' end,
    true
  )
  on conflict (id) do update set
    email = excluded.email,
    full_name = coalesce(nullif(excluded.full_name, ''), public.profiles.full_name),
    updated_at = now();
  return new;
end;
$$;

drop trigger if exists on_auth_user_created_create_profile on auth.users;
create trigger on_auth_user_created_create_profile
  after insert on auth.users
  for each row execute function public.handle_new_user_profile();

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists touch_profiles_updated_at on public.profiles;
create trigger touch_profiles_updated_at before update on public.profiles for each row execute function public.touch_updated_at();

drop trigger if exists touch_settings_updated_at on public.settings;
create trigger touch_settings_updated_at before update on public.settings for each row execute function public.touch_updated_at();

drop trigger if exists touch_categories_updated_at on public.categories;
create trigger touch_categories_updated_at before update on public.categories for each row execute function public.touch_updated_at();

drop trigger if exists touch_products_updated_at on public.products;
create trigger touch_products_updated_at before update on public.products for each row execute function public.touch_updated_at();

create or replace function public.current_app_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select p.role
  from public.profiles p
  where p.id = auth.uid()
    and p.is_active = true
  limit 1;
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(public.current_app_role() = 'admin', false);
$$;

create or replace function public.can_manage_content()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(public.current_app_role() in ('admin', 'content_manager'), false);
$$;

