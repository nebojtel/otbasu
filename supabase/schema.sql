-- Profiles
DROP POLICY IF EXISTS "profiles_select_own_or_admin" ON public.profiles;
CREATE POLICY "profiles_select_own_or_admin" ON public.profiles
  FOR SELECT USING (auth.uid() = id OR public.is_admin());

DROP POLICY IF EXISTS "profiles_update_admin" ON public.profiles;
CREATE POLICY "profiles_update_admin" ON public.profiles
  FOR UPDATE USING (public.is_admin()) WITH CHECK (public.is_admin());

-- Settings
DROP POLICY IF EXISTS "settings_public_read" ON public.settings;
CREATE POLICY "settings_public_read" ON public.settings
  FOR SELECT USING (true);

DROP POLICY IF EXISTS "settings_admin_write" ON public.settings;
CREATE POLICY "settings_admin_write" ON public.settings
  FOR ALL USING (public.is_admin()) WITH CHECK (public.is_admin());

-- Categories
DROP POLICY IF EXISTS "categories_public_read_active" ON public.categories;
CREATE POLICY "categories_public_read_active" ON public.categories
  FOR SELECT USING (is_active = true OR public.can_manage_content());

DROP POLICY IF EXISTS "categories_manage_content" ON public.categories;
CREATE POLICY "categories_manage_content" ON public.categories
  FOR ALL USING (public.can_manage_content()) WITH CHECK (public.can_manage_content());

-- Products
DROP POLICY IF EXISTS "products_public_read_active" ON public.products;
CREATE POLICY "products_public_read_active" ON public.products
  FOR SELECT USING (status = 'active' OR public.can_manage_content());

DROP POLICY IF EXISTS "products_manage_content" ON public.products;
CREATE POLICY "products_manage_content" ON public.products
  FOR ALL USING (public.can_manage_content()) WITH CHECK (public.can_manage_content());

-- Analytics
DROP POLICY IF EXISTS "analytics_public_insert" ON public.analytics_events;
CREATE POLICY "analytics_public_insert" ON public.analytics_events
  FOR INSERT WITH CHECK (event_type in ('product_view', 'video_click', 'kaspi_click', 'tab_click', 'filter_change', 'gallery_open'));

DROP POLICY IF EXISTS "analytics_admin_read" ON public.analytics_events;
CREATE POLICY "analytics_admin_read" ON public.analytics_events
  FOR SELECT USING (public.can_manage_content());

DROP POLICY IF EXISTS "analytics_admin_delete" ON public.analytics_events;
CREATE POLICY "analytics_admin_delete" ON public.analytics_events
  FOR DELETE USING (public.is_admin());

